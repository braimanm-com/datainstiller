package datainstiller.data;

public class PatternUnmarshalException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public PatternUnmarshalException() {
		super();
	}

	public PatternUnmarshalException(String message, Throwable cause) {
		super(message, cause);
	}

	public PatternUnmarshalException(String message) {
		super(message);
	}

	public PatternUnmarshalException(Throwable cause) {
		super(cause);
	}
	
}
